type Content struct {
	Color      []color.Color
	FontWidth  int
	FontHeight int
	Length     int
	Charset    []byte
}

func NewContent() *Content {
	c := &Content{}
	c.Color = []color.Color{color.Black}
	c.Length = Default_Length
	return c
}

func (c *Content) SetLength(len int) *Content {
	c.Length = len
	return c
}

// 在Charset中生成Length长度的随机码
func (c *Content) Build() string {
	if c.Length <= 0 {
		c.Length = Default_Length
	}
	charset := ""

	rand.Seed(time.Now().UnixNano() - 66)
	for i := c.Length; i > 0; i-- {
		charset += string(c.Charset[RandRange(len(c.Charset))])
	}
	return charset
}

